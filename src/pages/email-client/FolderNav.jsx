export default function FolderNav({ activeFolder, emails, onFolderChange }) {
  const counts = countAllUnreadCounts(emails)

  return (
    <aside className="ec-sidebar">
      <ul className="ec-folder-list">
        {Object.entries(FOLDER_META).map(([key, meta]) => (
          <li
            key={key}
            className={`ec-folder-item ${activeFolder === key ? 'active' : ''}`}
            onClick={() => onFolderChange(key)}
          >
            <span className="ec-folder-icon">
              <FolderIcon type={meta.icon} />
            </span>
            <span className="ec-folder-name">{meta.name}</span>
            {counts[key] > 0 && (
              <span className="ec-folder-count">{counts[key]}</span>
            )}
          </li>
        ))}
      </ul>
    </aside>
  )
}
